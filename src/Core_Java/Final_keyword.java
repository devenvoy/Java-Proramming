package Core_Java;

public class Final_keyword {
	public static void main(String[] args) {
		
		final double PI = 3.14159;				
		
		// it can only be assign once 
		
		System.out.println(PI);
		
		final String name;
		
		name = "Devansh";
	
		System.out.println(name);
		
//		cannot change it value again
//		name ="prince";
	}
}

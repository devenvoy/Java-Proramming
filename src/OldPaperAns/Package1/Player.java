package OldPaperAns.Package1;

public class Player {

    private String name;
    private String teamName;

    public Player(String pname, String pTeamName) {
        this.name = pname;
        this.teamName = pTeamName;
    }

    public String getName() {
        return name;
    }

    public String getTeamName() {
        return teamName;
    }


}
